        public Hero Class()
        {
            if (game.Champion.GetType() == typeof(Captain))
            {
                return new Captain(game);
            }
            else if (game.Champion.GetType() == typeof(Swordsman))
            {
                return new Swordsman(game);
            }
            else if (game.Champion.GetType() == typeof(Drunkard))
            {
                return new Drunkard(game);
            }
            else if (game.Champion.GetType() == typeof(Gunslinger))
            {
                return new Gunslinger(game);
            }
            else if (game.Champion.GetType() == typeof(Rogue))
            {
                return new Rogue(game);
            }
            else { return new Captain(game); }
        }

